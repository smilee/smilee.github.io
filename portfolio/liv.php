	 		<article id="abstract">
	 			<h3>Abstract</h3>
	 			<p>Liv is an interactive creature. Just like a real creature, Liv needs movement and social attention to stay
healthy. If you take good care of Liv, he will stay happy and healthy.<br><br>Liv is created for the new oncology centre in Utrecht, The Princes Máxima Centre. Staying in the hospital for a long time is a negative experience for the children, especially when you rather want to play outside instead of lying in bed all day. One of the comforts is knowing that you are not the only one in the hospital.<br><br>This is where Liv comes in. Every child gets the chance to keep an eye on Liv. Once in a while Liv asks you to play with it. By playing actively with Liv, he will become happy and healthy.</p>
