	 		<article id="abstract">
	 			<h3>Abstract</h3>
	 			<p>S-Manager is a manager for your daily life. The system is composed of two parts: the band and the app. The band is capable of ECG as well as measuring the heart and the breathing rate of the user. The app is connected to the band so the user can check the measurement results. When the user is under severe stress, the app alarms the user. The user can use the measuring data as medical records. Also, the user can manage their schedule and check milestones for work. The users can get some coupons related to their interests when they achieve a goal.</p>
