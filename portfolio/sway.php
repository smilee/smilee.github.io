	 		<article id="background" class="cf">
	 			<h3>Project Background</h3>
	 			<section id="context">
	 				<h4>Context</h4>
	 				<ul>
	 					<li>Many students at KAIST know little-to-nothing about departments they do not belong to. The fact that each department has their own way of numbering the courses does not help with the situation. With the absence of a standardized notational system for the courses, students will be confused when trying to figure out the relationships between the courses from their course codes alone. Furthermore, not all departments provide information on this matter. Sway aims to tackle these problems by bringing information from all departments into one place in the same format.</li>
	 				</ul>
	 			</section>
	 			<section id="goal">
	 				<h4>Goal</h4>
